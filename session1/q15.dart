// What does it mean for Dart to be a statically-typed language, and how does it impact the way variables are used? 

// the answer >>

// Since Dart is a statically typed language, this helps improve the performance of the application you are creating or working on. In other words, every variable you declare has its allocated space in RAM or storage. Since Dart enforces static typing, it ensures that no variable is placed incorrectly or occupies memory unnecessarily, which significantly enhances performance.
